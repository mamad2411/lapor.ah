export function getDashboardStats(laporan: Laporan[]) {
  const today = new Date().toISOString().slice(0, 10);
  const weekAgo = new Date(Date.now() - 7 * 86400000).toISOString();

  return {
    baruHariIni: laporan.filter((l) => l.createdAt.startsWith(today)).length,
    menunggu: laporan.filter((l) => l.status === "Belum di Proses").length,
    sedangProses: laporan.filter((l) => l.status === "Sedang di Proses").length,
    selesaiMinggu: laporan.filter(
      (l) => l.status === "Selesai" && l.updatedAt >= weekAgo
    ).length,
    darurat: laporan.filter((l) => l.prioritas === "Darurat" && l.status !== "Selesai").length,
    total: laporan.length,
  };
}
